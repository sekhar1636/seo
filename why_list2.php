<p>For musical theatre performers, you have the choice of doing any kind of monologue 
  you like in contrast with your song. It is also beneficial for singers to do 
  a monologue from a play, not from a musical. It's a minor detail, but if a very 
  good singer chooses a speech from a musical, there arises the question of whether 
  that performer is taking the acting part of his training seriously: you need 
  to read plays, too, not just watch movie musicals and listen to cast albums! 

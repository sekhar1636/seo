<h2>Over-used Songs & Monologues and Why You Should Avoid Them </h2>
<p><b>At the request of many of the participating theatres, we offer the following scenario and suggestions in choosing your audition material for StrawHat: </b></p>
<p>Imagine that you are not the person auditioning, but one of the people watching 
  the auditions. It is mid-February, and you have just spent the previous weekend 
  watching three days of combined auditions at the UPTA conference and plan to 
  spend the first weekend of March at the three days of combined auditions at 
  the SETC conference. You may even be headed for the NETC conference after that. 
  And there's still your own open call for one more day in NYC. This will total 
  well over 2,000 performers by the time you're done. Impressive, or daunting?</p>
<p>Imagine that you are sitting in the auditorium at the StrawHat Auditions from 10:00 in the morning till 6:00 in the evening or so, with only an hour for lunch and periodic 10-minute breaks between audition groups (shorter if the auditions are running behind.  See why we try to run a tight schedule?).  The seats are not terribly comfortable, the lighting is dim, and you've been doing this every spring for anywhere from 3 to 25 years.  Wouldn't you prefer to hear something well-presented that you haven't heard six times already today?  If it's a popular enough piece for six people to do it today, doesn't that suggest the possibility that six people have sung it every other day this season, let alone in past years?</p>
<p>As we often say, you should perform it if you do it brilliantly!  But the caveat is this: if you are brilliant at this piece, you're probably every bit as capable of knocking our socks off with something less-often heard.  Demonstrate that you take time in preparing your audition, not just in the performance of your selections, but in researching and choosing your selections to begin with.  If there is no question that this song is superior to anything else in showing you at your very best, by all means do it.</p>
<p>By the same token, if it's a piece you love but that doesn't really show you to your best, you should be searching for something else that let's your light shine brighter.  A perfectly fine rendition of an over-worked song can end up being dismissed just due to fatigue on the auditors' part ("Oh no, not that one again!" and we turn off our ears), whereas another choice might make an auditor perk up ("Hey!  I haven't heard that one in ages - I love that song!").</p>
<p>While it is not an explicit rule, we would simply urge you <u>for your own sake</u> to find alternatives to the following severely over-used musical selections:</p>


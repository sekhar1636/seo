<H1>What Should I Be Reading?</H1>

<P>Needless to say, there are any number of publications out there about acting and the other lively arts - but what are the vital information sources an actor can turn to? Here's a list of periodicals and books that are of enormous value to every actor:</P>

<H2>PERIODICALS available on a weekly or monthly basis:</H2>

<P><B>SHOW BUSINESS (weekly periodical)</B><BR>
Provides casting notices (some exclusive), arts-related news items, feature articles and theatrical reviews. Plus annual features on regional theatres, dinner theatres, combined auditions, etc. Subscription information: 212-986-4100.</P>

<P><B>BACKSTAGE (weekly periodical) </B><BR>
Provides casting notices, arts-related news items, feature columns on cabaret, dance; articles on everything from tax advice to temp jobs, headshots to health. Plus annual features on regional theatres, dinner theatres, combined auditions, etc. Subscription information: 1-800-458-7541</P>

<P><B>BACKSTAGE WEST (weekly periodical) </B><BR>
Los Angeles sister publication to Back Stage. For subscription info call 1-800-458-7541</P>

<P><B>THEATRICAL INDEX </B><BR>
  The Index publishes both weekly, but you can get a weekly, bi-weekly and monthly 
  subscription. For those of you who live in New York City, you can also buy a 
  copy at the Drama Book Store (now at 250 West 40th Street, between 7th and 8th 
  Avenues; 212-944-0595). The monthly subscription serves well for performers. 
  Provides listings of all shows currently running on and off Broadway, contact 
  info, including general manager, company manager, press representatives, running 
  schedules and ticket prices. Also provides info on future Broadway and Off-Broadway 
  plans, a run-down of premiere regional offerings, schedules for touring companies, 
  contact info for Broadway theatres, producers, and New York agents. The New 
  York Times calls Theatrical Index "Indispensable!" For subscription information: 
  email theatricalindex@nyc.rr.com. </P>

<P><B>AMERICAN THEATRE (monthly magazine) </B><BR>
Theatre Communications Group publishes this monthly, containing consistently excellent articles about new theatre, regional theatre, politics and theatre, international theatre, and a calendar of regional theatre production schedules. Twice a year or so, an issue will include the complete text of a new play (in the past these have included Stop/Kiss, Picasso at the Lapine Agile, The Waiting Room, and others.) Subscription information: 212-697-5230 or email custserv@tcg.org. </P>

<P><B>ROSS REPORTS (monthly) </B><BR>
The Ross Reports provide complete contact information for agents and casting directors, including who the agents are in each department (legit, TV, film, commercial) and how and where to contact them. Also provides listings of TV programming produced in NYC, along with casting contact info for both prime time and day time shows. An excellent tool for creating a mailing list when searching for an agent or promoting a performance. For subscription information write to Ross Reports, PO Box 2054, Marion, OH 43306-8154. </P>

<P><B>SHOW MUSIC MAGAZINE (quarterly) </B><BR>
Published by Goodspeed Musicals (the Goodspeed Opera House), SHOW MUSIC is dedicated to the music of musical theatre, past and present. Internationally acclaimed by professionals and fans as the premier magazine covering musical theatre around the world, SHOW MUSIC combines insightful interviews and reviews of productions, recordings, videos and books for a comprehensive quarterly look at news from on and off the musical stage. For subscription info: www.showmusic.org. </P>


<p> <b>ABOUT THE AUDITION TYPES:</b><BR>
  <br>
  <b>Actors & Singers:</b><BR>
  If you are selected for an appointment, you will have exactly ninety seconds 
  to present two pieces, so you should time and choose your material carefully. 
  Singers should begin with their musical selection followed by their monologue. 
  A professional piano accompanist is provided. If you are not regularly cast 
  in musicals, and you do not honestly sing to a performance standard, it is our 
  experience that you can only harm your chances by trying to bluff your way through 
  a musical audition. Most participating companies perform at least one musical 
  in their season, and therefore only a limited number - usually one third of 
  those attending - will audition or hire non-singers. Therefore, the number of 
  audition slots available to non-musical talent is restricted. Full information 
  on material and presentation will be sent with your appointment notification.</P>

<P><b>Dancers Who Sing:</b><BR>
  Dancers Who Sing auditions will take place on the last day of auditions only. Space is limited 
  to Dancer/Singer candidates with musical theatre experience and dance training in all disciplines. Selected 
  performers will be taught a combination at 1:00 p.m. which they will present 
  to casting reps beginning at 2:00. Dancers must be prepared to sing their best 
  16 bars of an up-tempo piece after presenting the dance combination.</P>

<P><b>Stand-By Appointments:</b><BR>
  Stand-bys step in at short notice to take the place of a cancellation or "no 
  show." Stand-by slots are available, but are strictly limited.<br>
  <br>
  If you indicated that you will accept a stand-by appointment and you are selected, 
  you will be asked to appear for only one of the days you indicated on your application. 
  Standbys receive confirmation of their audition day, just like a regular audition. 
  You must be available for the entire   audition day, 
  as you will step in at short notice to take the place of a cancellation or &quot;no 
  show&quot;. <b>Stand-bys who check in for their appointed audition are guaranteed 
  an audition</b> and their pictures and resumes will appear in that day&#146;s 
  book. <br>
  <br>
  Only those applicants who are selected and invited will be allowed to come in 
  on stand-by. StrawHat does not accept walk-ins. Stand-bys have a designated 
  waiting area backstage where they remain on call.</P>

<p><b>Callbacks:</b><BR>
  Callback lists are posted regularly throughout the day. Some theatres prefer 
  to contact performers directly at a later date, but most hold their preliminary 
  callbacks at StrawHat. You should plan to remain for the rest of the 
  day of your audition, since callbacks will continue into the evening hours. 
  If you are called back, you may also be asked to attend a group dance call at 
  the end of the day, in addition to the theatres' individual callbacks. Dancers 
  who are called back may be asked to attend the evening dance call as well.</p>

<P><b>Notification of Registration, Audition:</b><br>
  Once your complete application has been received and processed,
  you will receive notification by both mail and email (Mail Chimp).  
  
  Audition appointment confirmation is also sent by both mail and email. 
  If you are selected for an audition, the notice will confirm all the details 
  of the audition. 
  If you are not selected for an audition, a mailed notice will contain
  your returned audition fee. <br>
  <br>
  Once you are selected to audition there will be an additional charge of $25.00 
  to change your appointment time.</p>



<P><b>Mail the complete package to: StrawHat Auditions, 1771 Post Road East, #315, 
  Westport, CT 06880. Good luck!</b></p>


	 <H3>I Got An Audition! Now What (continued)</H3> 
	 <P><B>What Should I Bring With Me?</B></P> 
	 <P>It's a long day, and no joke - for everybody. So be prepared with some
		things you may - or may not - need.</P> 
	 <UL> 
		<LI>Your music - in a binder or in some form that will stand up on the
		  piano. Have your cuts clearly marked along with any other tempo notations.
		  Ideally with an edited piece, you've photocopied the pages you need so you can
		  scribble at will, and have cut and pasted together only what the pianist needs
		  to read.</LI> 
	 </UL> 
	 <P></P> 
	 <UL> 
		<LI>10 to 12 current headshots with resumes attached to them back to back
		  (At callbacks, some theatres may ask for them).</LI> 
	 </UL> 
	 <P></P> 
	 <UL> 
		<LI>Several pens/pencils and a notebook. You'll want to take legible
		  notes at callbacks - who you spoke to, what they've said - to supplement the
		  info provided in the StrawHat Actors Info Pack you'll receive. You'll want to
		  be as informed as possible when you're pondering all of your job offers a week
		  later. Retain those notes and Info Packs to begin developing your career
		  network. A polite follow-up note: "It was a pleasure auditioning for you at
		  StrawHat&#133;" is very gracious - and often remembered.</LI> 
	 </UL> 
	 <P></P> 
	 <UL> 
		<LI><B>If You Cannot Stay For Late Callbacks Due to a Conflict</B> bring
		  copies of the show flyer or postcard to leave with a note of explanation to the
		  theatres that called you back. The fact that you are currently cast in a show
		  only confirms that they should be interested in you.</LI> 
	 </UL> 
	 <P></P> 
	 <UL> 
		<LI>A toilet kit - hairbrush, comb, etc. As the day drags on, you'll want
		  to freshen up and look presentable.</LI> 
	 </UL> 
	 <P></P> 
	 <UL> 
		<LI>Clothing for dance call. And shoes. If you don't have dance gear, a
		  T-shirt and warm-up pants and sneakers are fine. Socks are too slippery, while
		  bare feet run the risk of being stepped on by the high heel of someone else's
		  character shoe. 
		  <P></P> </LI> 
		<LI>If you're a singer and you don't study dance - <B>START TAKING
		  CLASSES NOW</B>. Even the leads need to know their right feet from their left.
		  <P></P></LI> 
		<LI>A good paperback book to read during the long waits. </LI> 
	 </UL> 
	 <P><B>Make Sure Your Name, Address and Phone Number</B> are on <B>ALL</B>
		your personal belongings, and put a luggage tag on your bag. In the past we've
		found everything from a brand new pair of character shoes to a complete
		portfolio of audition songs to a really gross and sweaty leotard on the floor
		in a bathroom. <B>Keep track of your stuff!</B> We don't have a lost and
		found.</P> 
	 <P>There is a designated area that serves as a waiting lounge for
		performers, but <B>please take note</B>: You are responsible for your own
		belongings, and that includes cleaning up after yourself. If you are found with
		food, you are expected to dispose of your trash in the appropriate manner. If
		you have a beverage, you must be sure to cap the bottle whenever you set it
		down. Our staff will promptly dispose of foodstuffs and drinks left unattended.
		We are all guests in the audition facility. Treat the space with respect.</P> 
	 <P>Take these tips to heart, start getting yourself ready, and get out
		there and land that job of your dreams. Summer stock memories and summer stock
		friendships will be with you the rest of your life. Break a Leg!</P> 

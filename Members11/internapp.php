<H1>What is the Difference Between an Apprentice and an Intern?</H1>
<P>(Please note: this list was first compiled in 2009. We have not updaed the list since. We offer it as a starting point and feel free to email us with your corrections)<br>
<br>
The broadest definition is that apprenticeships are unpaid positions, and 
  internships offer some sort of a stipend. Both of these positions may require 
  crew work, i.e. assist with set construction or painting, wardrobe detail, etc. 
  However there is a wide spectrum of how each individual theater defines these 
  programs in regards to compensation, responsibility and categories offered. 
  For example, the Ogunquit Playhouse in Maine has internships for Tech Staff 
  only, where the Olney Theatre Center in Maryland has internships available in 
  all areas. (check out www.olnytheatres.org/opportunities.htm for an on-line 
  application). <BR>
  <BR>
  Also, the compensation varies from theater to theater: some offer a weekly stipend, 
  some offer room and board, others only offer housing (such as Capitol Theatre 
  Summerstage), or some offer all three. A variety also offer EMC (Equity Membership 
  Candidate) weeks and/or college credit either along with or in lieu of monetary 
  compensation. <BR>
  <BR>
  Your best bet is to check with the theaters in order to get the most up-to-date 
  information regarding the opportunities offered. All of the theaters listed 
  below have either links to web-sites, e-mail addresses for you to request information, 
  or phone numbers.</P>

<h1>Why "The Dreaded List?"</h1>
<h2>Over-used Songs & Monologues and Why You Should Avoid Them<br><br>
  <font size="3"><b>At the request of many of the participating theatres, we offer 
  the following suggestions in choosing your audition material for StrawHat:</b></font></h2>
<p>Up-tempo pieces are encouraged, and a boffo 16 bars will leave us wanting more. 
  Perfect.<br>
  Do not do pieces from plays with titles you can't pronounce (&quot;Lapin Agile&quot;, 
  anyone?).<br>
  Please avoid speeches that are directly sexual in content or intent.<br>
  Shakespeare is not typically produced in summer theatre, and therefore may not 
  be your best choice for combined auditions.<br>
  The question arose last season: &quot;Why are all these women so <i>angry</i>?&quot;<br>
</p>
<p>Casting representatives at combined auditions end up seeing 2,000 or more performers 
  each season. Imagine being one of those reps. Wouldn't you prefer to hear something 
  well-presented that you haven't heard six times already today? If it's a popular 
  enough piece for six people to do it today, doesn't that suggest the possibility 
  that six people have sung it every other day this audition season, let alone 
  in past years?</p>
<p>As we often say, you should perform it if you do it brilliantly! But the caveat 
  is this: if you are brilliant at this piece, you're probably every bit as capable 
  of knocking our socks off with something less-often heard. Demonstrate that 
  you take time in preparing your audition -- not just in the performance of your 
  selections, but in researching and choosing your selections to begin with. If 
  there is no question that this song is superior to anything else in showing 
  you at your very best, by all means do it. </p>
<p>By the same token, if it's a piece you love but doesn't really show you to 
  your best, you should be searching for something else that let's your light 
  shine brighter. A perfectly fine rendition of an over-worked song can end up 
  being dismissed just due to fatigue on the auditors' part ("Oh no, not that 
  one again!" and we turn off our ears), whereas another choice might make an 
  auditor perk up ("Hey! I haven't heard that one in ages - I love that song!").</p>

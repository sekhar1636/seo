<P>In fact, you should probably beware of singing anything predictable from Annie, 
  Baby, Secret Garden, Children of Eden, the movie Fame, Godspell, Newsies, Thoroughly 
  Modern Millie, Phantom of the Opera, any Disney animated film produced since 
  1986, and anything from an original cast album released within the past two 
  years. </p>
<p>We also strongly suggest that for combined audition purposes you avoid material 
  by Stephen Sondheim and the newer musical theatre composers, i.e. Michael John 
  LaChuisa, Ricky Ian Gordon, et al. Although these composers are highly respected 
  and popular, their material is difficult to perform even under the best of circumstances, 
  as they often comprise difficult and/or multiple time signatures and complex 
  passages with extensive arpeggios. Bear in mind the physical conditions:</p>
<ol>
<li>You will have at best 30 seconds or so to speak with the accompanist about tempi, cuts, etc. (See our web article on tips for prepping your music). </li>
  <li>The piano is positioned off-stage and behind you when you audition - the 
    pianist cannot see your face, nor can you make eye-contact with him should 
    there be a problem. </li>
  <li>As the auditions take place on a stage, the accompanist is using only a dim 
    music lamp on the piano to read by. Thus, very "black" music (meaning lots 
    and lots of notes to be played - extensive arpeggios, etc.) is extremely difficult 
    to read. </li>
<li>Complex, obscure music can distract us from listening to you as opposed to evaluating the song itself.  If the auditors are busy wondering what the piece is and whether they like it or not, they aren't paying much attention to you as a performer.</li>
</ol>
<p>A final note on this: let's face it, in summer stock you are far more likely 
  to be performing in Oklahoma! or My Fair Lady than in Marie Christine or Parade. 
  There are thousands upon thousands of standard musical theatre pieces that show 
  range, emotion, versatility, humor, legato - the works! That's why the material 
  is classic. And if you can inhabit a beloved standard and make it your own, 
  you are a long way toward proving yourself as a performer to be reckoned with. 
  SO, sing the song that's right for you, not the song for the role you think 
  you're right for.</p>


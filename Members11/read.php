<H1>What Should I Be Reading?</H1>

<P>Needless to say, there is a lot of stuff out there about acting and the other lively arts - but what are the vital information sources an actor can turn to? Here's a list of resources that are of enormous value to every actor:</P>

<H2>WEBSITES  and PERIODICALS:</H2>

<P><B>SHOWBUSINESS.COM</B><BR>
Provides arts-related news items, feature articles and theatrical reviews. Plus annual features on regional theatres, dinner theatres, combined auditions, etc. </P>

<P><B>BACKSTAGE.COM and BackStage magazine (weekly periodical) </B><BR>
Covering theatre, television, dance, and film throughout the US, BackStage provides casting notices, arts-related news items, feature columns on cabaret, dance; articles on everything from tax advice to temp jobs, headshots to health. Plus annual features on regional theatres, dinner theatres, combined auditions, etc. Subscribe for online access and set yourself up for emailed Daily Casting notices, newsletter features, etc. Weekly print edition also available through the mail and at newsstands. </P>


<P><B>THEATRICALINDEX.COM </B><BR>
  The Index publishes weekly, but you can get a weekly, bi-weekly or monthly 
  subscription, with web service offered for free at every subscription level. 
  For those of you who live in New York City, you can also buy a 
  copy at the Drama Book Store. The monthly subscription serves well for performers. 
  Provides listings of all shows currently running on and off Broadway, contact 
  info, including general manager, company manager, press representatives, running 
  schedules and ticket prices. Also provides info on future Broadway and Off-Broadway 
  plans, a run-down of premiere regional offerings, schedules for touring companies, 
  contact info for Broadway theatres, producers, and New York agents. The New 
  York Times calls Theatrical Index "Indispensable!" </P>

<P><B>AMERICAN THEATRE (monthly magazine) </B><BR>
Theatre Communications Group publishes this monthly, containing consistently excellent articles about new theatre, regional theatre, politics and theatre, international theatre, and a calendar of regional theatre production schedules. Twice a year or so, an issue will include the complete text of a new play (in the past these have included Stop/Kiss, Picasso at the Lapine Agile, The Waiting Room, and others.) Subscription information: 212-697-5230 or email custserv@tcg.org. </P>


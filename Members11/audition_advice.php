	 <H3>I Got An Audition! Now What?</H3> 
	 <P>You're on your way to chasing some great opportunities in summer
		theatre, so start preparing for your audition now. You have 90 seconds to
		present 2 pieces, a song and a monologue or two contrasting monologues. It
		doesn't seem like very much time, but we all know how fast we register our
		first impressions socially. It's the same in auditions and it's absolutely true
		that we know if you "have it" or not within the first phrase you sing - or
		speak. So how do you get called back at StrawHat?</P> 
	 <H3>PRACTICE! PRACTICE! PRACTICE!</H3> 
	 <P>Find a stopwatch or a clock with a sweep second hand. Edit your audition
		selections and run them over and over again. The more you rehearse your pieces
		back to back - just as you'll present them - the more comfortable you'll
		become. <B>REHEARSE YOUR INTRODUCTION</B> and run it along with your pieces in
		rehearsal:</P> 
	 
<P ALIGN="center">"Hi, I'm Susie Q, 10:45. My availability is May 25th to September 
  4th. I'll be doing 'I Cain't Say No' and a speech from Don't Drink the Water."<BR>
   <B>OR</B><BR> "Hello, I'm John
		Smith and my time is 3:32. My pieces are from The Water Engine and Irma
		Vep."</P> 
	 <P ALIGN="center"><B>RUN YOUR PIECES</B><BR> Then say, "Thank you, I'm John
		Smith, 3:32."</P> 
	 <UL> 
		<LI>If you don't rehearse your intro, you may stumble over the words -
		  that could throw you enough that you're less confident in your
		  presentation.</LI> 
		<LI>If you don't time your rehearsals, you can't possibly be sure that
		  you'll come in under 90 seconds.<BR> </LI> 
	 </UL> 
	 <P ALIGN="center"><B>WE WILL CUT YOU OFF</B><BR> As graciously as possible,
		to be sure, but we promise it will happen if necessary.</P> 
	 <H2 ALIGN="left">How Do You Make the Most of the Time You Have?</H2> 
	 
<UL>
  <LI>Figure out the very least you need for your start on your song - Ideally 
    a single bell-tone or a two-bar vamp. That takes none of your time and gives 
    an impression of efficiency and professionalism. If your intro goes more than 
    four bars, the stage managers reserve the right to start the clock. 
    <P>Bless his heart - one fella a while back had a whole 16 bar intro before 
      he sang - a ballad! Slow, slow, slow - so the 16 bars alone used up about 
      20 seconds before he ever opened his mouth. He got cut off before he started 
      his first verse.</P>
  </LI>
  <LI>There is an "X" in the 'hot spot' Downstage Center. The light is best there 
    - so start there and end there. You are free to use the stage as you wish, 
    but show yourself to advantage - by being in the best light!<BR>
    <SPAN CLASS="advice">&middot; </SPAN> </LI>
  <LI>There is a chair Upstage Center, should you wish to use one. If you bring 
    it downstage, then you must strike it back to it's upstage position. PRACTICE! 
    PRACTICE! PRACTICE! moving the chair DS before or during your intro, then 
    striking it back US after your "thank you."</LI>
</UL> 

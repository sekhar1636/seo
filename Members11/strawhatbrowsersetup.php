<h2 align="left">How to Set Up Your Browser to Use the Searches</h2>
<P><b>Using tabs in your browser, you can organize your search pages. </b>When 
  you select a search, the results will be organized in a tab. Select one of the 
  results (Actor or Tech) and that is placed in the next tab to the right. Click 
  on a resume link, and the resume is placed in the next tab to the right.</P>
<P><b>Microsoft Explorer: </b> Look at the upper <b>RIGHT</b> corner of your Explorer 
  Browser and you will see a <b>GEAR</b> icon. Click on it and you will see 
  Internet Options. Go to <b>TABS</b> and click <b>Settings</b>. You will see 
  a number of click boxes. Choose the following:</P>
  
<UL>
  <LI>Enable Tabbed Browsing (requires restarting Internet Explorer).</LI>
  <LI>When a new tab is opened, select "'The new tab page'".</LI>
  <LI>When a pop-up is encountered, select "'Always open pop-ups in a new tab'".</LI>
  <LI>Open links from other programs in either a new tab in the current window 
    or a new window</LI>
</UL>

<P> <b>Mozilla Firefox:</b>On the upper <b>LEFT</b> corner, click <b>"'Firefox'"</b>, 
  then Options. Select <b>TABS</b> and choose the following: </P>
  
<UL>
  <LI>Open new window in a new tab.</li>
  <LI>Warn me when closing multiple tabs.</li>
</UL>

<P> <b>Google Chrome: </b>On the upper <b>RIGHT</b> corner, click the &quot;<b>WRENCH</b>&quot; 
  icon. Chrome opens the windows automatically. If you have problems, click the 
  <b>Wrench</b>, then <b>Options</b> - <b>Preferences</b> - <b>Under the Hood</b>. 
  The default setting is "'Do not allow popups'". If your Tabs don't work you may 
  have to change the setting to allow popups. </P>

<P> <b>Apple Safari:</b> On the upper <b>RIGHT</b> corner, click on the <b>"'GEAR'"</b> 
  icon Select Preferences, Tabs and choose the following: </P>
  <UL>
  <LI>Open pages in tabs instead of windows "'Automatically'".</LI>
  <LI>When a new tab or window opens, make it active.</LI>
</UL>

<H1>Resumes</H1>

<P>Your picture & resume are your calling card - the first thing a casting director 
  sees and often the ONLY tool used to determine your qualifications. Therefore, 
  they need to represent you as professionally as possible. Theatrical resumes 
  are not the same as traditional business resumes, so in the following, you'll 
  find our suggestions and some explanations, followed by a couple of examples. 
<P><b>White paper</b> is preferable for a very simple reason: Black ink on white 
  paper is the most extreme contrast of colors and makes for the easiest reading. 
  When auditioning, you want casting directors to be able to glance easily down 
  at the page while you're performing, not straining to make out your past credits. 
  Keep in mind that casting directors are looking at many, many resumes and auditions 
  through the course of an audition series. Eyestrain is a major factor in fatigue 
  - and fatigue quickly translates into irritability. </P>

<P><b>Font Style:</b> Again - as you are auditioning, an interested producer will 
  want to refer to your resume, but you want him to be watching you! Fancy fonts 
  are tremendous fun to play with, but they can be detrimental to your audition. 
  Choose a variation based on the basic Roman alphabet: Times New Roman, Helvetica, 
  Gil Sans, Arial, Bookman Old Style, etc. Italics are unnecessary. Play around 
  with your font selections on your p.c. and you'll get the picture. </P>

<P><b>Point Size:</b> Please don't use use anything smaller than 11 point, and 
  12 point is preferable. Smaller point sizes are very hard to read. If your resume 
  is packed to the gills at 11 point, then congratulations! You've already got 
  a lot of experience. But it also means that it's time to start doing some pruning 
  from the different categories.</P>
<P><b>Format the page</b> so it can be cropped to match your 8x10 photo's size. 
  Standard paper size is 8-1/2 x 11, and the excess edges will get ragged and 
  tatty looking - unprofessional. </P>
<ul>
  <li>Set your left and top margins for 0.5&quot;, your right margin for 1&quot;, 
    and your bottom margin for 1.5&quot; and then you can easily trim the excess 
    from the right and bottom. Office supply stores usually have a table top paper 
    cutter in the copy center. Use it.</li>
  <li>Credits should be listed in set columns: Play, Role, then Theatre (unless 
    you elect our example #2). Set tabs at intervals to make the columns line 
    up neatly.</li>
  <li>Categories can help break up a long list of credits, or beef up a resume 
    that may seem thin. You can separate Musical Theatre and Theatre, or Summer 
    Stock and Educational, Educational and Community. We offers two prototypes 
    at the end of this article.</li>
</ul>
